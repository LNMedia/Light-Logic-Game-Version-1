                        <span style="z-index: 2; position: fixed; display: inline-block; margin-left: -2px; margin-top: -2px;  text-align: center; font-size: 1.5em; border-bottom: solid 1px white; width: 100%; padding-top: 5px; padding-bottom: 10px; background: rgba(0,0,0,0.8)">lange Fassung</span>
                        <div class="longVersionText">
                            <span style="display: inline-block; text-align: justify; font-size: 0.8em; padding-top: 5px;">
                                Willkommen beim Light Logic Game, einem faszinierenden 2D-Puzzlespiel, das sowohl deine strategischen als auch deine logischen Fähigkeiten herausfordert.
                                In diesem Spiel besteht dein Ziel darin, alle Lichter auf dem Spielfeld auszuschalten, um das Rätsel zu lösen.
                                Jede Runde fordert dich auf neue Weise heraus, da du sorgfältig überlegen musst, welche Felder du in welcher Reihenfolge anklickst, um die gewünschte Wirkung zu erzielen.
                                Jedes Licht, das du ausschaltest, kann die umliegenden Felder beeinflussen, und so entsteht ein komplexes Zusammenspiel, welches genaues Planen und Vorausschau erfordert.
                            </span>
                            <br>
                            <br>
                            <span style="color: rgba(150,0,100);">Ziel des Spiels:</span>
                            <span style="display: inline-block; text-align: justify; font-size: 0.8em; padding-top: 5px;">
                                Die Spielmechanik basiert darauf, dass ein Klick auf ein Feld nicht nur das angeklickte Feld, sondern auch die 4 direkt benachbarten Felder umschaltet.
                                Das bedeutet, dass leuchtende Felder dunkel werden und dunkle Felder aufleuchten.
                                Diese einfache Regel führt zu einer Vielzahl von strategischen Überlegungen, da jeder Zug weitreichende Folgen haben kann.<br>
                                Das Hauptziel des Spiels ist es, alle Lichter auf dem Spielfeld auszuschalten.
                                Ein Feld kann entweder leuchten oder dunkel sein.
                                Wenn alle Felder dunkel sind, hast du das Spiel gewonnen.
                            </span>
                            <br>
                            <br>
                            <span style="color: rgba(150,0,100);">Spielfelder:</span>
                            <span style="display: inline-block; text-align: justify; font-size: 0.8em; padding-top: 5px;">
                                Teste deine Fähigkeiten in verschiedenen Spielfeldvarianten & -größen, die jeweils ihre eigenen Herausforderungen bieten.
                                Ob quadratische, vertikale oder horizontale Felder – jede Variante erfordert eine angepasste Herangehensweise und bietet neue Möglichkeiten, deine Problemlösungs-fähigkeiten zu schärfen.
                                Zu Beginn jeder Runde werden die Felder zufällig gemischt, sodass einige Felder leuchten und andere dunkel sind.<br>
                                Sei gewarnt, nicht jedes Spielfeld ist für jedes Gerät verfügbar.
                                Stelle sicher, dass dein Gerät die ausgewählte Spielfeldgröße und -variante unterstützt, bevor du ins Spiel einsteigst.
                            </span>
                            <br>
                            <br>
                            <span style="color: rgba(150,0,100);">Spielverlauf:</span>
                            <span style="display: inline-block; text-align: justify; font-size: 0.8em; padding-top: 5px;">
                                <oblige style="color: rgba(250,50,0,0.8)">Starten des Spiels:</oblige>
                                Klicke auf den "Spiel Starten"-Button im Hauptmenü, um einen Schwierigkeitsgrad zu wählen und wähle einen aus.
                                Das Spielfeld wird angezeigt, und die Lichter werden zufällig verteilt.
                                <br>
                                <oblige style="color: rgba(250,50,0,0.8)">Spielzüge:</oblige>
                                Wähle ein Feld aus, indem du darauf klickst.
                                Beim Klicken auf ein Feld werden dieses und die angrenzenden Felder (oben, unten, links, rechts) umgeschaltet:
                                Leuchtende Felder werden dunkel.
                                Dunkle Felder beginnen zu leuchten.
                                <br>
                                <oblige style="color: rgba(250,50,0,0.8)">Strategie:</oblige>
                                Überlege sorgfältig, bevor du auf ein Feld klickst, da jeder Klick mehrere Felder beeinflusst.
                                Finde die optimale Reihenfolge der Klicks, um alle Lichter auszuschalten.
                            </span>
                            <br>
                            <br>
                            <span style="color: rgba(150,0,100);">Zusätzliche Funktionen:</span>
                            <span style="display: inline-block; text-align: justify; font-size: 0.8em; padding-top: 5px;">
                                <oblige style="color: rgba(250,50,0,0.8)">Rückgängig machen:</oblige>
                                Du kannst den letzten Zug rückgängig machen, wenn du einen Fehler gemacht hast.
                                <br>
                                <oblige style="color: rgba(250,50,0,0.8)">Neustart:</oblige>
                                Wenn du feststeckst, kannst du das aktuelle Level neu starten.
                                <br>
                                <oblige style="color: rgba(250,50,0,0.8)">Strategie:</oblige>
                                Eine Bestenliste zeigt die besten Spieler und höchsten Punktzahlen an, um den Wettbewerbsgeist zu fördern.
                            </span>
                            <br>
                            <br>
                            <span style="color: rgba(150,0,100);">Tipps und Tricks:</span>
                            <span style="display: inline-block; text-align: justify; font-size: 0.8em; padding-top: 5px;">
                                Versuche, das Zentrum des Spielfelds zu kontrollieren, da es die meisten angrenzenden Felder beeinflusst.
                                Denke mehrere Züge voraus, um die Lichter effizienter auszuschalten.
                                Übung macht den Meister! Je mehr du spielst, desto besser wirst du die Muster und Strategien erkennen.
                            </span>
