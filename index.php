            document.addEventListener('fullscreenchange', handleFullscreenChange);
            document.addEventListener('webkitfullscreenchange', handleFullscreenChange); // Für Safari
            document.addEventListener('mozfullscreenchange', handleFullscreenChange);    // Für Firefox
            document.addEventListener('msfullscreenchange', handleFullscreenChange);     // Für IE/Edge
            function handleFullscreenChange() {
                if (!document.fullscreenElement &&
                    !document.webkitFullscreenElement &&
                    !document.mozFullScreenElement &&
                    !document.msFullscreenElement) {
                    // Der Vollbildmodus wurde beendet
                    document.getElementById('fullscreen').checked = false;
                    document.querySelector('.fullscreen').style.background = "rgba(0,0,0,0.5)";
                }
            }
            function toggleFullscreen() {
                if (document.getElementById('fullscreen').checked) {
                    if (document.documentElement.requestFullscreen) {
                        document.documentElement.requestFullscreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,50,0,0.5)";
                    } else if (document.documentElement.mozRequestFullScreen) { /* Firefox */
                        document.documentElement.mozRequestFullScreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,50,0,0.5)";
                    } else if (document.documentElement.webkitRequestFullscreen) { /* Chrome, Safari and Opera */
                        document.documentElement.webkitRequestFullscreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,50,0,0.5)";
                    } else if (document.documentElement.msRequestFullscreen) { /* IE/Edge */
                        document.documentElement.msRequestFullscreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,50,0,0.5)";
                    }
                } else {
                    if (document.exitFullscreen) {
                        document.exitFullscreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,0,0,0.5)";
                    } else if (document.mozCancelFullScreen) { /* Firefox */
                        document.mozCancelFullScreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,0,0,0.5)";
                    } else if (document.webkitExitFullscreen) { /* Chrome, Safari and Opera */
                        document.webkitExitFullscreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,0,0,0.5)";
                    } else if (document.msExitFullscreen) { /* IE/Edge */
                        document.msExitFullscreen();
                        document.querySelector('.fullscreen').style.background = "rgba(0,0,0,0.5)";
                    }
